Node* findIntersection(Node* head1, Node* head2)
{
    // Your Code Here
    unordered_map<int ,int> ump;
    Node* dummy=new Node(-1);
    Node* ans=dummy;
    while(head1){
        ump[head1->data]++;
        head1=head1->next;
    }
    while(head2){
        if(ump.find(head2->data)!=ump.end()){
            ans->next=new Node(head2->data);
            ans=ans->next;
            ump.erase(head2->data);
        }
        head2=head2->next;
    }
    return dummy->next;
}